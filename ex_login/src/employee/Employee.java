package employee;

public class Employee {
	private int num;
	private int employee_num;
	private String employee_rank;
	private String employee_name;
	private String employee_ssn;
	private String employee_day;
	private String department;
	private String employee_phone;
	private String address;
	private int gender;
	private String hobby;
	private String specialty;
	private String homenumber;
	
	
	public String getHomenumber() {
		return homenumber;
	}
	public void setHomenumber(String homenumber) {
		this.homenumber = homenumber;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getEmployee_num() {
		return employee_num;
	}
	public void setEmployee_num(int employee_num) {
		this.employee_num = employee_num;
	}
	public String getEmployee_rank() {
		return employee_rank;
	}
	public void setEmployee_rank(String employee_rank) {
		this.employee_rank = employee_rank;
	}
	public String getEmployee_name() {
		return employee_name;
	}
	public void setEmployee_name(String employee_name) {
		this.employee_name = employee_name;
	}
	public String getEmployee_ssn() {
		return employee_ssn;
	}
	public void setEmployee_ssn(String employee_ssn) {
		this.employee_ssn = employee_ssn;
	}
	public String getEmployee_day() {
		return employee_day;
	}
	public void setEmployee_day(String employee_day) {
		this.employee_day = employee_day;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getEmployee_phone() {
		return employee_phone;
	}
	public void setEmployee_phone(String employee_phone) {
		this.employee_phone = employee_phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getHobby() {
		return hobby;
	}
	public void setHobby(String hobby) {
		this.hobby = hobby;
	}
	public String getSpecialty() {
		return specialty;
	}
	public void setSpecialty(String specialty) {
		this.specialty = specialty;
	}
}
